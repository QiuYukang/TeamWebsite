    <p>互联与路由技术实验室的主要研究方向为协议转换、隧道和协议覆盖等异构网络互联技术和方法，网络设备互联的工程技术及组网技术，互联网中的编址与寻址技术，路由算法和协议，移动、组播路由技术，路由与高速交换技术等。</p>
    <p>随着国际互联网的兴起和蓬勃发展，网络互联和路由技术成为通信专业课程教学热点。互联与路由技术实验室面向通信专业的本科生和研究生，为专业配套课程如 TCP/IP 协议原理、局域网与城域网、网络系统工程、网络管理、网络安全、网络应用系统、网络软件设计等课程提供相关实验项目。</p>
    <p>实验室拥有大量各主流厂商的路由器、三层交换机、以太网交换机、接入服务器、网络测试仪等互联网设备。 </p>
    <p>目前实验室已设计和开放的实验项目包括： 1、原理性实验：如 ALOHA 与 SLOT ALOHA 协议模拟实验、地址解析协议原理， IP 分组投递、 TCP 连接实验，距离矢量算法原理实验、链路状态算法原理实验等。 2、操作型实验，如交换机配置、路由器配置、 VLAN 配置、 IP 子网与掩码、网络连通性测试与路径跟踪实验等。 3、设计型实验，如通信协议原理实验，网络软件设计实验等，路由协议设计等。 </p>
    <p>主要设备提供商： Cisco （思科），华为，安捷伦，迈普，博达，桑达等。</p>

