    <p>实验室目前拥有实验室拥有多种类型的无线组网设备，涵盖多个在 WLAN 中执行的行业标准，其中主流标准有： （1）IEEE802.11b （2）IEEE 802.11g （3）IEEE 802.11a </p>
    <p>设备类型有：（1）无线路由器 （2）无线接入点―― AP （3）具有 PCMCIA 接口的无线网卡 （4）具有 USB2.0 接口的无线网卡</p>
    <p>在拥有设备种类和技术先进性方面，实验室目前处于国内领先水平。可以为教师和学生提供 WLAN 技术领域中以下方面的实验项目： （1）WLAN 规划设计 （2） WLAN 组网、接入、认证 （3）无线移动： WLAN 组网、 Mobile IP 等 （4）无线路由： Ad Hoc 、 MANET 、 Mobile IP 等 （5）无线网 QOS ：多媒体通信技术 （6）无线网管理：网络管理 （7）WLAN 信道技术：编解码及芯片设计 s</p>
    <p>WLAN 技术是网络通信领域里的新兴技术，其技术体制和知识体系尚不十分成熟，国内也鲜见高校为本科生开设相关实验。实验室紧跟技术发展面向本科生设计无线局域网组网和认证实验，配合相关课程内容进行。今后还将在这个基础上开发出更多的实验项目，逐渐形成一套独立的、先进的、可操作性强的系列无线网路实验。</p>
    <p>以下是《接入网技术》这门四川省精品课程所拥有的实验条件。</p>
    <p>依托我校<strong>国家“通信与信息系统教学实验中心”的综合接入实验室、骨干传输系统实验室、互连与路由技术实验室先进的网络设备和丰富的资源，可以构建融合多种主流接入技术、统一用户管理、全真的电信级综合接入实验平台，可以构建从接入网到核心网，从局域网到广域网的大型通信网络环境。网络设备技术先进，实验环境国内高校一流。</strong></p>
    <p>综合接入网实验室设备包括：ADSL接入、EPON接入、以太接入，WLAN接入等多种主流接入系统设备，还有统一接入控制设备BAS，集中用户管理设备AAA（认证、授权、记账）服务器。骨干传输系统实验室于2002年启动建设，经过与深圳华为公司合作和获得捐赠，共计人民币近800万元。设备包括：622Mbps的SDH传输系统、程控交换机系统、拨号接入服务系统等。互连与路由实验室具有国内外著名企业的路由器和交换机40多台套。先进的设备、丰富的资源为接入网技术实验提供了强有力的保障。</p>

